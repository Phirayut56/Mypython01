# คำสั่งแสดงผลข้อมูลใดๆ ทางหน้าจอ ใช้ฟังก์ชัน print()
# String
print('Hello')
print("Hi")
print('''Hey''')
print("""Hum""")

# Number
print(123)          # Integer/int
print(456.789)      # Float

# Boolean ตรรกะ มีแค่าจริง True หรือเท็จ False
print(True)         
print(False)        

# Expression นิจพน์ อะไรก็ตามที่มี process การทำงาน เช่น การคำนวณ
print(10 + 20 + 5)
print(10 * 50 / 2)
